package com.example.testand;

public class user {
        String company;
        String product;

        public user() {
        }

        public user(String company, String product) {
            this.company = company;
            this.product = product;
        }

        public String getCompany() {
            return company;
        }

        public String getProduct() {
            return product;
        }

}
